<div class="container my-5">
    <h1>Maestría en Gerencia de Empresas Agropecuarias</h1>
    <p><strong>Régimen de Estudio:</strong> Cuatrimestral.</p>
    <p><strong>Grado:</strong> Magister Scientiarum en Gerencia de Empresas Agropecuarias</p>
    <p>Las organizaciones se desenvuelven en un ambiente de constantes cambios en aspectos económicos, tecnológicos, sociales, políticos y competitivos, entre otros, que afectan la eficiencia y eficacia de las organizaciones para alcanzar sus metas y objetivos. En la búsqueda por adaptarse a esos cambios las organizaciones han desarrollado diferentes estrategias y soluciones. Una de ellas es el desarrollo organizacional que tiene como objetivo mejorar su productividad y eficacia, así como la calidad de vida de la sociedad, mediante el uso de teorías y prácticas que facilitan el cambio sobre la base de conocimientos relacionados con la conducta.</p>
    
    <h4>Objetivos:</h4>
    <ul>
        <li>Dotar a los participantes de una visión general de las teorías del desarrollo organizacional.</li>
        <li>Crear un espacio de reflexión para comprender cómo los cambios internos y externos afectan a las organizaciones.</li>
        <li>Brindar herramientas para ser el gestor de las adaptaciones y cambios dentro de las organizaciones.</li>
    </ul>
</div>

